#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*pt_s;

	pt_s = (unsigned char*)s;
	while (n)
	{
		*pt_s = '\0';
		pt_s++;
		n--;
	}
}
